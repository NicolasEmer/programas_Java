    private void btDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeletarActionPerformed
        IDao<Endereco, Integer> dao = DaoFactory.createEnderecoDao();

        try {
            dao.delete(Integer.parseInt(this.textoID.getText()));
            JOptionPane.showMessageDialog(null, "Deletado.", "Concluído", JOptionPane.INFORMATION_MESSAGE);
        } catch (NotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível deletar.", "Problema", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btDeletarActionPerformed

    private void BtAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAtualizarActionPerformed
        IDao<Endereco, Integer> dao = DaoFactory.createEnderecoDao();

        Integer id = Integer.parseInt(this.textoID.getText());
        String rua = this.textoRua.getText();
        String bai = this.textoBairro.getText();
        int num = Integer.parseInt(this.TextoNumero.getText());
        String cid = (this.textoCidade.getText());
        Estado es = (Estado)this.ComboEstado.getSelectedItem();
        

        boolean update = (this.en.getPk() == 0);

        this.en.setPk(id);
        this.en.setRua(rua);
        this.en.setBairro(bai);
        this.en.setNumero(num);
        this.en.setCidade(cid);
        this.en.setEstado(es);

        try {
            if (update) {
                dao.update(en);
            }else{
                System.out.println("Erro com o ID.");
            }
        } catch (NotFoundException ex) {
            Logger.getLogger(Tela_cad_endereco.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
    }//GEN-LAST:event_BtAtualizarActionPerformed

