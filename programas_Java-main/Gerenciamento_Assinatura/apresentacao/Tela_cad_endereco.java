    private void btCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCriarActionPerformed

        Integer id = Integer.parseInt(this.textoID.getText());
        String rua = this.textoRua.getText();
        String bai = this.textoBairro.getText();
        int numero = Integer.parseInt(this.TextoNumero.getText());
        String cid = (this.textoCidade.getText());
        Estado es = (Estado)this.ComboEstado.getSelectedItem();
        

        boolean create = (this.en.getPk() == null);

        this.en.setPk(id);
        this.en.setRua(rua);
        this.en.setBairro(bai);
        this.en.setNumero(numero);
        this.en.setCidade(cid);
        this.en.setEstado(es);

        IDao<Endereco, Integer> dao = DaoFactory.createEnderecoDao();

        try {
            if (create) {
                dao.create(en);
            }
        } catch (KeyViolationException | InvalidKeyException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        dispose();
    }//GEN-LAST:event_btCriarActionPerformed

