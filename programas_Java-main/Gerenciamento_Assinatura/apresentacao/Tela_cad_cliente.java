    public void initCombos(ArrayList<Forma_pagamento> pagValidas, ArrayList<Assinatura> assiValidas, ArrayList<Endereco> enValidos)
    {
        this.Combo_assinaturas.removeAllItems();
        this.Combo_forma_pag.removeAllItems();
        this.Combo_Endereco.removeAllItems();
        for (Assinatura assi: assiValidas)
        {
            this.Combo_assinaturas.addItem(assi);
        }
        
        for (Forma_pagamento forma: pagValidas)
        {
            this.Combo_forma_pag.addItem(forma);
        }
        
        for (Endereco ende: enValidos)
        {
            this.Combo_Endereco.addItem(ende);
        }
    }

