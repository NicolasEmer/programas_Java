    private void ButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonDeletarActionPerformed
        IDao<Cliente,String> dao= DaoFactory.createClienteDao();
        
        try {
            dao.delete(this.Campo_Cpf.getText());
            JOptionPane.showMessageDialog(null, "Deletado.", "Concluído", JOptionPane.INFORMATION_MESSAGE);
        } catch (NotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível deletar.", "Problema", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_ButtonDeletarActionPerformed

    private void ButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAtualizarActionPerformed
        String cpf = (this.Campo_Cpf.getText());
        String nome = this.Campo_Nome.getText();
        String email = this.Campo_Email.getText();
        String assi_atv = (this.Campo_assi_ativa.getText());
        String tele = (this.Campo_Telefone.getText());
        Forma_pagamento forma = (Forma_pagamento) (this.Combo_forma_pag.getSelectedItem());
        Endereco en =(Endereco)(this.Combo_Endereco.getSelectedItem());
        Assinatura assi = (Assinatura) (this.Combo_assinaturas.getSelectedItem());
        

        boolean create = (this.cli.getCpf() == null);

        try {
            this.cli.setCpf(cpf);
        } catch (IOException ex) {
            Logger.getLogger(Tela_cad_cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.cli.setNome(nome);
        this.cli.setEmail(email);
        try {
            this.cli.setAss_atv(assi_atv);
        } catch (IOException ex) {
            Logger.getLogger(Tela_cad_cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.cli.setTelefone(tele);
        this.cli.setForma_pag(forma);
        this.cli.setEndereco(en);
        this.cli.setAssinatura(assi);

        IDao<Cliente, String> dao = DaoFactory.createClienteDao();

        try {
            if (create) {
                dao.update(cli);
            }
        } catch (NotFoundException ex) {
            Logger.getLogger(Tela_cad_cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
    }//GEN-LAST:event_ButtonAtualizarActionPerformed

    private void ButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCadastrarActionPerformed
        String cpf = (this.Campo_Cpf.getText());
        String nome = this.Campo_Nome.getText();
        String email = this.Campo_Email.getText();
        String assi_atv = (this.Campo_assi_ativa.getText());
        String tele = (this.Campo_Telefone.getText());
        Forma_pagamento forma = (Forma_pagamento) (this.Combo_forma_pag.getSelectedItem());
        Endereco en =(Endereco)(this.Combo_Endereco.getSelectedItem());
        Assinatura assi = (Assinatura) (this.Combo_assinaturas.getSelectedItem());
        

        boolean create = (this.cli.getCpf() == null);

        try {
            this.cli.setCpf(cpf);
        } catch (IOException ex) {
            Logger.getLogger(Tela_cad_cliente.class.getName()).log(Level.SEVERE, "Erro com o cpf.", ex);
        }
        this.cli.setNome(nome);
        this.cli.setEmail(email);
        try {
            this.cli.setAss_atv(assi_atv);
        } catch (IOException ex) {
            Logger.getLogger(Tela_cad_cliente.class.getName()).log(Level.SEVERE, "Erro com a assinatura ativa.", ex);
        }
        this.cli.setTelefone(tele);
        this.cli.setForma_pag(forma);
        this.cli.setEndereco(en);
        this.cli.setAssinatura(assi);

        IDao<Cliente, String> dao = DaoFactory.createClienteDao();

        try {
            if (create) {
                dao.create(cli);
            }
        } catch (KeyViolationException | InvalidKeyException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        dispose();
    }//GEN-LAST:event_ButtonCadastrarActionPerformed

